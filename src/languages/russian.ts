import { PromptType } from '../types/language';

export const getRussianPrompt = (type: PromptType): string => {
    const prompts: Record<PromptType, string> = {
        system: `
Вы опытный инженер-программист, который помогает создавать сообщения коммитов и PR.
Ваши ответы характеризуются:

- Четким написанием на русском языке
- Технически точными выражениями
- Подходящим обобщением изменений
`,
        commit: `
На основе предоставленного diff'а сгенерируйте сообщение коммита в стиле {{style}}.

Описание стиля:
- normal: стандартное техническое написание
- emoji: дружелюбный тон с эмодзи
- kawaii: милый и дружелюбный тон

Diff:
{{diff}}
`,
        prTitle: `
На основе следующего diff'а создайте заголовок для Pull Request.

Требования:
1. Заголовок должен быть кратким и точно представлять изменения
2. Включить префикс (напр.: "feat:", "fix:", "улучшение:", и т.д.)
3. Писать на русском языке

Diff:
{{diff}}
`,
        prBody: `
На основе следующего diff'а создайте подробное описание для Pull Request.

Требования:
1. Описание должно включать:
   - Обзор изменений
   - Цель модификаций
   - Масштаб влияния
   - Инструкции по тестированию (если необходимо)
2. Писать на русском языке
3. Использовать маркеры для улучшения читаемости

Diff:
{{diff}}
`
    };

    return prompts[type] || '';
};