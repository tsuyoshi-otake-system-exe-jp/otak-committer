import { PromptType } from '../types/language';

export const getHebrewPrompt = (type: PromptType): string => {
    const prompts: Record<PromptType, string> = {
        system: `
אני מהנדס תוכנה מנוסה שעוזר ביצירת הודעות commit ו-PR.
הפלט שלי מתאפיין ב:

- עברית ברורה ותמציתית
- ביטויים מדויקים טכנית
- סיכום נאות של השינויים
`,
        commit: `
על בסיס ה-diff המסופק, צור הודעת commit בסגנון {{style}}.

תיאור הסגנון:
- normal: כתיבה טכנית סטנדרטית
- emoji: טון ידידותי עם אימוג'ים
- kawaii: טון חמוד וידידותי

Diff:
{{diff}}
`,
        prTitle: `
על בסיס ה-diff הבא, צור כותרת ל-Pull Request.

דרישות:
1. הכותרת צריכה להיות תמציתית ולייצג במדויק את השינויים
2. כלול תחילית (למשל "Feature:", "Fix:", "Improvement:", וכו')

Diff:
{{diff}}
`,
        prBody: `
על בסיס ה-diff הבא, צור תיאור מפורט ל-Pull Request.

# סקירה כללית
- הסבר קצר על התכונות או התיקונים שיושמו
- מטרה והקשר של השינויים
- הגישה הטכנית שאומצה

# נקודות מפתח לסקירה
- אזורים הדורשים תשומת לב מיוחדת מהסוקרים
- החלטות עיצוב חשובות
- שיקולי ביצועים ותחזוקה

# פרטי השינויים
- שינויים עיקריים שיושמו
- רכיבים ופונקציונליות שהושפעו
- שינויי תלויות (אם יש)

# הערות נוספות
- שיקולי פריסה
- השפעה על פונקציונליות קיימת
- משתני תצורה או סביבה נדרשים

Diff:
{{diff}}
`
    };

    return prompts[type] || '';
};