import { PromptType } from '../types/language';

export const getHebrewPrompt = (type: PromptType): string => {
    const prompts: Record<PromptType, string> = {
        system: `
אתה מהנדס תוכנה מנוסה המסייע ביצירת הודעות commit ו-PR.
הפלט שלך מתאפיין ב:

- כתיבה ברורה בעברית מימין לשמאל
- ביטויים מדויקים טכנית
- סיכום נאות של השינויים
`,
        commit: `
על בסיס ה-diff המסופק, צור הודעת commit בסגנון {{style}}.

תיאור הסגנון:
- normal: כתיבה טכנית סטנדרטית
- emoji: טון ידידותי עם אימוג'י
- kawaii: טון חמוד וידידותי

Diff:
{{diff}}
`,
        prTitle: `
על בסיס ה-diff הבא, צור כותרת ל-Pull Request.

דרישות:
1. הכותרת צריכה להיות תמציתית ולייצג את השינויים במדויק
2. כלול תחילית (למשל: "תכונה:", "תיקון:", "שיפור:" וכו')
3. כתוב בעברית

Diff:
{{diff}}
`,
        prBody: `
על בסיס ה-diff הבא, צור תיאור מפורט ל-Pull Request.

דרישות:
1. התיאור צריך לכלול:
   - סקירה כללית של השינויים
   - מטרת השינויים
   - היקף ההשפעה
   - הוראות בדיקה (אם נדרש)
2. כתוב בעברית
3. השתמש בנקודות לשיפור הקריאות

Diff:
{{diff}}
`
    };

    return prompts[type] || '';
};